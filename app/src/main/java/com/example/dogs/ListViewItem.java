package com.example.dogs;

public class ListViewItem {

    private int id;
    private String name;

    //이미지를 숫자로 저장
    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
